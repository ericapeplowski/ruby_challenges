weather = 'sunny'

case weather
when 'rainy'
    puts "Don't forget your umbrella!"
when 'windy'
    puts "Better put your hair up or you'll be eating it!"
when 'sunny'
    puts "Better where sunglasses or a hat."
when 'cloudy'
    puts "You might want a sweater. I might be a little chilly."
when 'snowy'
    puts "Stay home! Otherwise wear some sturdy boots and be careful out there!"
else
    puts "Hmm, not sure but you could probably just wear your usual jeans and flipfops."
end
